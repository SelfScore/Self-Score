// Helper function to get score-based content for a level
export const getScoreBasedContent = (level: number, score: number): ScoreBasedContent | null => {
  let contentArray: ScoreBasedContent[];

  switch (level) {
    case 1:
      contentArray = level1ScoreContent;
      break;
    case 2:
      contentArray = level2ScoreContent;
      break;
    case 3:
      contentArray = level3ScoreContent;
      break;
    default:
      return null;
  }

  return contentArray.find(c => score >= c.minScore && score <= c.maxScore) || contentArray[0];
};

